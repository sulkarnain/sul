import math

# fungsi luas kubus 
def luasKubus (s):
    return s * s

# fungsi keliling kubus
def kelilingKubus (s):
    return 12 * s

# fungsi luas persegi panjang
def luasPersegiPanjang (p, l):
    return p * l

# fungsi keliling persegi panjang
def kelilingpersegiPanjang (p, l):
    return 2 * (p+l)

# fungsi luas balok
def luasBalok (pl, pt, lt):
    return 2 (pl + pt + lt)
              
# fungsi keliling balok
def kelilingBalok (p, l, t):
    return 4 ( p + l + t)

# fungsi luas lingkaran
def luasLingkaran (r):
    return math.pi * r * r

# fungsi keliling lingkaran
def kelilingLingkaran (r):
    return 2 * math.pi * r

# fungsi luas jajargenjang 
def luasJajarGenjang (a, t):
    return a * t

# fungsi keliling jajargenjang
def kelilingJajarGenjang (a, b):
    return 2 * (a + b)

# fungsi luas segitiga
def luasSegitiga (a, t):
    return (a * t) / 2

# fungsi keliling segitiga
def kelilingSegitiga (a, b, c):
    return a + b + c

